//add a Return item
    private void addReturnItem(String returnID, String borrowID, String returnDate, String isLate, String numberOfDateLate, String fineAmount, String isFinePaymentPaid, String itemID) {
        DbConnection db = new DbConnection();

        String sql = "INSERT INTO returnitem(return_id,borrow_id,returnDate,isLate,numberOfDateLate,fineAmount,isFinePaymentPaid) VALUES(?,?,?,?,?,?,?)";
        String updateSql = "UPDATE borrowitem,returnitem,item SET borrowitem.isReturn=?,item.isAvailable=? WHERE returnitem.borrow_id=borrowitem.borrow_id AND borrowitem.item_id=item.item_id AND borrowitem.borrow_id=? AND item.item_id=?;";//to update item availability status

        try {
            PreparedStatement statement = db.getDbConnection().prepareStatement(sql);
            PreparedStatement statementUpdate = db.getDbConnection().prepareStatement(updateSql);

            statement.setString(1, returnID);
            statement.setString(2, borrowID);
            statement.setString(3, returnDate);
            statement.setString(4, isLate);
            statement.setString(5, numberOfDateLate);
            statement.setString(6, fineAmount);
            statement.setString(7, isFinePaymentPaid);

            statementUpdate.setString(1, "Yes");
            statementUpdate.setString(2, "Yes");
            statementUpdate.setString(3, borrowID);
            statementUpdate.setString(4, itemID);

            int row = statement.executeUpdate();
            int rowUpdate = statementUpdate.executeUpdate();

            if (row > 0 && rowUpdate > 0) {
                JOptionPane.showMessageDialog(null, "Return Item add sucessfully!!!", "Return Item", JOptionPane.INFORMATION_MESSAGE);
                backDashboard(userName);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Item Return Error", JOptionPane.ERROR_MESSAGE);
        }

    }

