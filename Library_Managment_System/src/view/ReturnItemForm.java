    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        borrowID = return_add_getBorrowID.getText();
        itemID = return_add_getReturnID.getText();

        try {
            emptyFieldChecker(borrowID);//check empty field

            IDChecker checker = new IDChecker();
            checker.checkID(borrowID, "borrow_id", "borrowitem"); //check id validatity
            checker.checkID(itemID, "item_id", "item");

            DbConnection db = new DbConnection();
            String sql = "SELECT dueDate FROM borrowitem WHERE borrow_id=?;";

            PreparedStatement statement = db.getDbConnection().prepareStatement(sql);
            statement.setString(1, borrowID);
            ResultSet result = statement.executeQuery();
            if (result.next()) {
                dueDate = result.getString("dueDate"); // get item due date to calculate fine

                numberOfDateLate = calulateLateDates(dueDate, returnDate);

                if (numberOfDateLate == 0) {
                    isLate = "No";
                } else {
                    isLate = "Yes";
                }

                fineAmount = numberOfDateLate * 10;

                return_add_setReturnDate.setText(returnDate);
                return_add_setIsLate.setText(isLate);
                return_add_setnumberOfDateLate.setText(Integer.toString(numberOfDateLate));
                return_add_setFineAmount.setText(Integer.toString(fineAmount));
                return_add_isPaymentPaidNo.setSelected(true);

                returnBtn.setVisible(true);
            }

        } catch (EmptyFieldException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Return Item Empty Field Error", JOptionPane.ERROR_MESSAGE);
        } catch (IDnotValidException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "ID check error", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "check fine payment error", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_jButton4ActionPerformed

