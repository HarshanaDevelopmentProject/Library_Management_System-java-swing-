    private void searchReturnItem(String searchId) throws NotFoundException {
        String returnID;
        String borrowID;
        String returnDate;
        String isLate;
        String numberOfDateLate;
        String fineAmount;
        String finePaymentPaid;

        DbConnection db = new DbConnection();
        String sql = "SELECT * FROM returnitem WHERE return_id=?;";
        try {
            PreparedStatement statement = db.getDbConnection().prepareStatement(sql);
            statement.setString(1, searchId);
            ResultSet result = statement.executeQuery();

            if (result.next()) {  // to find member
                returnID = result.getString("return_id");
                borrowID = result.getString("borrow_id");
                returnDate = result.getString("returnDate");
                isLate = result.getString("isLate");
                numberOfDateLate = result.getString("numberOfDateLate");
                fineAmount = result.getString("fineAmount");
                finePaymentPaid = result.getString("isFinePaymentPaid");

                return_find_setReturnID.setText(returnID);
                return_find_setBorrowID.setText(borrowID);
                return_find_setReturnDate.setText(returnDate);
                return_find_setIsLate.setText(isLate);
                return_find_setNumberOfDateLate.setText(numberOfDateLate);
                return_find_setFineAmount.setText(fineAmount);
                return_find_setFinePaymentPaid.setText(finePaymentPaid);

            } else {
                throw new NotFoundException(searchId + " not Found");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Find Return Item Error", JOptionPane.ERROR_MESSAGE);
        }
    }

