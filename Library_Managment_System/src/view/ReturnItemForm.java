    //check nuber of date is late
    private int calulateLateDates(String dueDate, String returnDate) {

        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy/MM/dd");

        LocalDate due = LocalDate.parse(dueDate, formatter);
        LocalDate returned = LocalDate.parse(returnDate, formatter);

        // Calculate the difference in days (returned - due)
        long lateDays = ChronoUnit.DAYS.between(due, returned);

        if (lateDays > 0) {
            return (int) lateDays;
        } else {
            return 0;
        }
    }

