    private void loadTable() {
        String paymentID;
        String returnID;
        String fineAmount;
        String paymentDate;
        String paymentTime;
        String finePaymentPaid;

        DbConnection db = new DbConnection();
        String sql = "SELECT * FROM finepayment;";
        try {
            PreparedStatement statement = db.getDbConnection().prepareStatement(sql);
            ResultSet result = statement.executeQuery();
            while (result.next()) {  // to llop all data
                paymentID = result.getString("payment_id");
                returnID = result.getString("return_id");
                fineAmount = result.getString("fineAmount");
                paymentDate = result.getString("paymentDate");
                paymentTime = result.getString("paymentTime");
                finePaymentPaid = result.getString("isPaymentComplete");

                //show data in table
                String data[] = {paymentID, returnID, fineAmount, paymentDate, paymentTime, finePaymentPaid};
                DefaultTableModel table = (DefaultTableModel) jTable1.getModel();
                table.addRow(data);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Load Table Error", JOptionPane.ERROR_MESSAGE);
        }

    }

