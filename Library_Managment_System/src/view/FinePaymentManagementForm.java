//implement to search fine payment from payment id
    private void searchFinePayment(String searchId) throws NotFoundException {
        String paymentID;
        String returnID;
        String fineAmount;
        String paymentDate;
        String paymentTime;
        String finePaymentPaid;

        DbConnection db = new DbConnection();
        String sql = "SELECT * FROM finepayment WHERE payment_id=?;";
        try {
            PreparedStatement statement = db.getDbConnection().prepareStatement(sql);
            statement.setString(1, searchId);
            ResultSet result = statement.executeQuery();

            if (result.next()) {  // to find payment
                paymentID = result.getString("payment_id");
                returnID = result.getString("return_id");
                fineAmount = result.getString("fineAmount");
                paymentDate = result.getString("paymentDate");
                paymentTime = result.getString("paymentTime");
                finePaymentPaid = result.getString("isPaymentComplete");

                payment_find_setPaymentID.setText(paymentID);
                payment_find_setReturnID.setText(returnID);
                payment_find_setFineAmount.setText(fineAmount);
                payment_find_setPaymentDate.setText(paymentDate);
                payment_find_setPaymentTime.setText(paymentTime);
                payment_find_setIsPaymentComplete.setText(finePaymentPaid);

            } else {
                throw new NotFoundException(searchId + " not Found");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Find fine payment Error", JOptionPane.ERROR_MESSAGE);
        }
    }

