    private void addFinePayment(String paymentID, String returnID, String fineAmount, String paymentDate, String paymentTime, String isPaymentComplete) {
        DbConnection db = new DbConnection();

        String sql = "INSERT INTO finepayment(payment_id,return_id,fineAmount,paymentDate,paymentTime,isPaymentComplete) VALUES(?,?,?,?,?,?);";

        try {
            PreparedStatement statement = db.getDbConnection().prepareStatement(sql);

            statement.setString(1, paymentID);
            statement.setString(2, returnID);
            statement.setString(3, fineAmount);
            statement.setString(4, paymentDate);
            statement.setString(5, paymentTime);
            statement.setString(6, isPaymentComplete);

            int row = statement.executeUpdate();

            if (row > 0) {
                JOptionPane.showMessageDialog(null, "Fine Payment add sucessfully!!!", "Fine Payment", JOptionPane.INFORMATION_MESSAGE);
                backDashboard(userName);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Fine Payment Error", JOptionPane.ERROR_MESSAGE);
        }

    }

