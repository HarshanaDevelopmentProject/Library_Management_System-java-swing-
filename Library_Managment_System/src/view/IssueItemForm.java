//implement to search issue item from issue id
    private void searchIssueItem(String searchId) throws NotFoundException {
        String issueID;
        String type;
        String issueDate;
        String dueDate;
        String isReturn;

        String memberID;
        String memberName;
        String memberEmail;
        String memberContact;

        String itemID;
        String title;
        String author;
        String publishYear;

        DbConnection db = new DbConnection();
        String sql = "SELECT * FROM borrowitem,member,item WHERE borrowitem.member_id=member.member_id AND borrowitem.item_id=item.item_id AND borrowitem.borrow_id=?;";
        try {
            PreparedStatement statement = db.getDbConnection().prepareStatement(sql);
            statement.setString(1, searchId);
            ResultSet result = statement.executeQuery();

            if (result.next()) {  // to find member
                issueID = result.getString("borrowitem.borrow_id");
                type = result.getString("borrowitem.type");
                issueDate = result.getString("borrowitem.borrowDate");
                dueDate = result.getString("borrowitem.dueDate");
                isReturn = result.getString("borrowitem.isReturn");

                memberID = result.getString("member.member_id");
                memberName = result.getString("member.name");
                memberEmail = result.getString("member.email");
                memberContact = result.getString("member.phoneNumber");

                itemID = result.getString("item.item_id");
                title = result.getString("item.title");
                author = result.getString("item.authorOrDirector");
                publishYear = result.getString("item.publicationYear");

                issue_find_setIssueID.setText(issueID);
                issue_find_setType.setText(type);
                issue_find_setIssueDate.setText(issueDate);
                issue_find_setDueDate.setText(dueDate);
                issue_find_setIsReturn.setText(isReturn);

                issue_find_setMemberID.setText(memberID);
                issue_find_setMemberName.setText(memberName);
                issue_find_setMemberEmail.setText(memberEmail);
                issue_find_setMemberContact.setText(memberContact);

                issue_find_setItemID.setText(itemID);
                issue_find_setItemTitle.setText(title);
                issue_find_setItemAuthor.setText(author);
                issue_find_setItemPublicationYear.setText(publishYear);

            } else {
                throw new NotFoundException(searchId + " not Found");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Find Issue Item Error", JOptionPane.ERROR_MESSAGE);
        }
    }

