    private void addIssueItem(String borrowID, String memberID, String itemID, String type, String borrowDate, String dueDate, String isReturn) {
        DbConnection db = new DbConnection();

        String sql = "INSERT INTO borrowitem(borrow_id,member_id,item_id,type,borrowDate,dueDate,isReturn) VALUES(?,?,?,?,?,?,?)";
        String itemStatusSql = "UPDATE item SET isAvailable=? WHERE item_id=?;";//to update item availability status

        try {
            PreparedStatement statement = db.getDbConnection().prepareStatement(sql);
            PreparedStatement statementItem = db.getDbConnection().prepareStatement(itemStatusSql);

            statement.setString(1, borrowID);
            statement.setString(2, memberID);
            statement.setString(3, itemID);
            statement.setString(4, type);
            statement.setString(5, borrowDate);
            statement.setString(6, dueDate);
            statement.setString(7, isReturn);

            statementItem.setString(1, "No");
            statementItem.setString(2, itemID);

            int row = statement.executeUpdate();
            int rowItem = statementItem.executeUpdate();

            if (row > 0 && rowItem > 0) {
                JOptionPane.showMessageDialog(null, "Issue Item add sucessfully!!!", "Issue Item", JOptionPane.INFORMATION_MESSAGE);
                backDashboard(userName);

            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Item Issue Error", JOptionPane.ERROR_MESSAGE);
        }

    }

